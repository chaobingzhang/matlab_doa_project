%% 直接运行！三维频率-时间-方位角（Z轴方位角+不规律离散点）
clear; clc; close all;

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 1. 生成不规律模拟数据（方位角随机波动，无串联）
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 维度参数（清晰无密集）
f_vec = 15:5:200;  % X轴：频率（15~200Hz，间隔5Hz，38个频点）
t_vec = 0:1:8;     % Y轴：时间（0~8秒，9个时间点）
az_center = 120;   % 方位角中心值（120°）
az_noise = 25;     % 方位角随机波动范围（±25°，模拟噪声，保证不规律）

% 生成三维不规律数据：每个频率-时间对应独立随机方位角（无关联，不串联）
Nf = length(f_vec);  % 38个频点
Nt = length(t_vec);  % 9个时间点
X = []; Y = []; Z = [];  % 存储三维坐标（逐个生成，保证不规律）

for f_idx = 1:Nf
    for t_idx = 1:Nt
        % 每个(f,t)对应独立的随机方位角（无规律，不串联）
        random_az = az_center + az_noise * randn();  % 正态分布波动
        X = [X, f_vec(f_idx)];  % 频率
        Y = [Y, t_vec(t_idx)];  % 时间
        Z = [Z, random_az];     % 方位角（Z轴，不规律波动）
    end
end

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 2. 绘制三维图（Z轴=方位角+不规律黑色点）
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
figure('NumberTitle', 'off', 'Name', '三维频率-时间-方位角展示图', 'Position', [100, 100, 1000, 800]);

% 核心：不规律黑色离散点（无串联、无配色，仅展示分布）
scatter3(X, Y, Z, 60, 'k', 'filled', 'MarkerEdgeColor', 'white', 'LineWidth', 0.5);

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% 3. 标签与美化（简洁专业）
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
xlabel('频率/Hz', 'FontSize', 13);
ylabel('时间/s', 'FontSize', 13);
zlabel('方位角（°）', 'FontSize', 13);  % Z轴=方位角
title('三维时频空分布展示（未加权水下噪声+白噪声）', 'FontSize', 14, 'FontWeight', 'bold');

% 优化视角与显示
grid on;        % 三维网格增强立体感
view(35, 40);   % 最优视角（避免遮挡，清晰看不规律分布）
axis tight;     % 紧凑显示，聚焦数据区域
set(gca, 'FontSize', 11);  % 刻度字体大小